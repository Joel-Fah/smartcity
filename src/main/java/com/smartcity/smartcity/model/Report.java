package com.smartcity.smartcity.model;

public class Report {
    private String reportName;

    // Constructor
    public Report(String reportName) {
        this.reportName = reportName;
    }

    // Getter
    public String getReportName() {
        return reportName;
    }

    // Setter
    public void setReportName(String reportName) {
        this.reportName = reportName;
    }
}
